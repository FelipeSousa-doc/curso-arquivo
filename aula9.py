adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2 # float (mesmo que coloque número inteiro)
print('Divisão', divisao)

divisao_inteira = 10 // 2.2  #número inteiro (só a parte antes da virgula)
print('Divisão inteira', divisao_inteira)

resto_divisao = 25 % 5 # Que é chamado de modulo
print('Modulo ', resto_divisao)

exponenciacao = 2 ** 4
print('Exponenciação', exponenciacao)

print(10 % 2 == 0)
print(16 % 8 == 0)
