#tipos int e float
#int -> número inteiro
#O tipo int representa qualquer número
#positivo ou negativo. int sem sinal é positivo.
print(10) #int
print(-10) #int
print(0) #int

#float é número com virgula(ponto)
print(11.9)
print(-11.9)
print(0.5)

#a função type mostra o tipo de valor de acordo com o python
print(type('Felipe'))
print(type(1))
print(type(1.90))

