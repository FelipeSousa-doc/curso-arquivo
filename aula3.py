#Docstring (''')
'''
Python= linguagem de programação 
Tipo de tipagem= Dinâmica/forte
str -> string -> texto
String são textos dentro de aspas
'''

#aspas simples
print('Olá, Mundo!')

#aspas duplas
print("José Antonio")

#Se quiser colocar aspas na string é só inverter as aspas que iniciou a str
print(2, '"josé antonio"')
print(6, "'malandro é malandro, né?'")

#Escape (O interpretador pula o caractere e lê o proximo, escape dele...)
print('José \"Antonio\"')

#r -> mostra o caractere de escape
print(r'José \"Antonio\"')
