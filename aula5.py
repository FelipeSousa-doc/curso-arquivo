# Tipo de dado bool (boolean)
# Ao questionar algo no programa,
# Só existe duas respostas possiveis:
# sim (true) ou não (false).
# existem vários operadores para questionar 
# dentre eles o ==, que é um operador lógico que 
# questiona se um valor é igual ao outro. 
print(10 == 10) #True (verdadeiro ou  sim)
print(5 == 5.1) #False (falso ou não)
print(type(5 == 5.1)) #False (falso ou não)

